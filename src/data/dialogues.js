export function جلب_حوار_مكيّف(المفتاح, الشخصية = 'المسافر') {
  const forms = صياغة_حسب_الشخصية(الشخصية);
  const deepGet = المفتاح.split('.').reduce((acc, k) => (acc ? acc[k] : null), dialogues);
  if (!Array.isArray(deepGet)) return [];
  return deepGet.map((line) => line
    .replaceAll('مررتَ', الشخصية === 'المسافرة' ? 'مررتِ' : 'مررتَ')
    .replaceAll('تجاوزتَ', الشخصية === 'المسافرة' ? 'تجاوزتِ' : 'تجاوزتَ')
    .replaceAll('تبقّت', 'تبقّت')
    .replaceAll('بلغتَ', الشخصية === 'المسافرة' ? 'بلغتِ' : 'بلغتَ')
    .replaceAll('اقتربتَ', الشخصية === 'المسافرة' ? 'اقتربتِ' : 'اقتربتَ')
    .replaceAll('كنتُ أنا واقفًا', الشخصية === 'المسافرة' ? 'كنتُ أنا واقفةً' : 'كنتُ أنا واقفًا')
    .replaceAll('اخترت', forms.اخترت || 'اخترت'));
}
